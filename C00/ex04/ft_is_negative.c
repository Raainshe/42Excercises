#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;

	if (n >= 0 || n == '\0')
	{
		p = 'P';
		write(1, &p, 1);
	}
	else
	{
		p = 'N';
		write(1, &p, 1);
	}
}
